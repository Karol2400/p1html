#Programa que imprime cadenas o listas en un ciclo for

palabra="Computadora"

for i in palabra:
    print(i)
    print("")
deportes=["Futbol", "basquetbol", "karate"]
for i in deportes:
    print(i)
    print("")
for i in range (1,8):
    print(i)
    print("")
for x in range (2, 30, 4):
    print(x)
    print("")